"""Post-process emergence predictions with proxy tolerance columns."""

from __future__ import annotations

import logging
import re
from dataclasses import dataclass
from typing import TYPE_CHECKING

import pandas as pd

from emergence.path_config import (
    CONFIG_PATH,
    get_required_string,
    load_stage_config,
    resolve_config_path,
)

if TYPE_CHECKING:
    from pathlib import Path

logger = logging.getLogger(__name__)

PREDICTION_COLUMN = "PREDICTIONS"
TRAY_COLUMN = "TRAY"
BOX_POSITION_COLUMN = "BOX_POSITION"
PREDICTION_PLUS_ONE_COLUMN = "prediction_+1"
PREDICTION_MINUS_ONE_COLUMN = "prediction_-1"
PREDICTION_PLUS_TWO_COLUMN = "prediction_+2"
PREDICTION_MINUS_TWO_COLUMN = "prediction_-2"
REQUIRED_PREDICTION_COLUMNS = (
    PREDICTION_COLUMN,
    TRAY_COLUMN,
    BOX_POSITION_COLUMN,
)
TOLERANCE_COLUMNS = (
    PREDICTION_PLUS_ONE_COLUMN,
    PREDICTION_MINUS_ONE_COLUMN,
    PREDICTION_PLUS_TWO_COLUMN,
    PREDICTION_MINUS_TWO_COLUMN,
)
BOX_COLUMN_PATTERN = re.compile(r"^BOX(?P<number>\d+)$")


@dataclass(frozen=True)
class PredictionPostprocessingPaths:
    """Filesystem paths required by the prediction post-processing stage."""

    input_dir: Path
    output_dir: Path
    time_steps: int
    data_range: int


@dataclass(frozen=True)
class PostprocessedPredictionOutputs:
    """Output files generated by prediction post-processing."""

    predictions_csv: Path
    predictions_xlsx: Path
    first_germination_csv: Path
    first_germination_xlsx: Path


def load_prediction_postprocessing_paths(
    config_path: Path = CONFIG_PATH,
) -> PredictionPostprocessingPaths:
    """Load prediction post-processing paths from shared TOML configuration.

    Args:
        config_path: Path to the TOML config file; defaults to ``CONFIG_PATH``.

    Returns:
        Populated ``PredictionPostprocessingPaths`` dataclass.

    Author:
        Jakub Vašák

    """
    context, emergence_config = load_stage_config("emergence_predictions", config_path)
    output_dir = resolve_config_path(
        context,
        get_required_string(
            emergence_config,
            "output_dir",
            context.config_path,
        ),
        "paths.emergence_predictions.output_dir",
        include_project_name=True,
    )

    return PredictionPostprocessingPaths(
        input_dir=output_dir,
        output_dir=output_dir,
        time_steps=context.time_steps,
        data_range=context.data_range,
    )


def prediction_file_suffix(time_steps: int, data_range: int) -> str:
    """Return the output filename suffix for one model window.

    Args:
        time_steps: Number of time steps in the model window.
        data_range: Data range of the model window.

    Returns:
        Suffix string in the form ``"<time_steps>-<data_range>"``.

    Author:
        Jakub Vašák

    """
    return f"{time_steps}-{data_range}"


def input_predictions_path(paths: PredictionPostprocessingPaths) -> Path:
    """Return the existing full-prediction CSV path for configured paths.

    Args:
        paths: Configured post-processing paths.

    Returns:
        Path to the predictions CSV produced by stage 4.

    Author:
        Jakub Vašák

    """
    suffix = prediction_file_suffix(paths.time_steps, paths.data_range)
    return paths.input_dir / f"predictions_model_{suffix}.csv"


def postprocessed_prediction_output_paths(
    paths: PredictionPostprocessingPaths,
) -> PostprocessedPredictionOutputs:
    """Return all post-processed output paths for configured paths.

    Args:
        paths: Configured post-processing paths.

    Returns:
        ``PostprocessedPredictionOutputs`` dataclass with output file paths.

    Author:
        Jakub Vašák

    """
    suffix = prediction_file_suffix(paths.time_steps, paths.data_range)
    return PostprocessedPredictionOutputs(
        predictions_csv=paths.output_dir
        / f"predictions_model_{suffix}_with_tolerance.csv",
        predictions_xlsx=paths.output_dir
        / f"predictions_model_{suffix}_with_tolerance.xlsx",
        first_germination_csv=paths.output_dir
        / f"first_germination_model_{suffix}_with_tolerance.csv",
        first_germination_xlsx=paths.output_dir
        / f"first_germination_model_{suffix}_with_tolerance.xlsx",
    )


def validate_prediction_schema(predictions: pd.DataFrame) -> None:
    """Validate columns and prediction values required for post-processing.

    Args:
        predictions: Full predictions DataFrame to validate.

    Raises:
        ValueError: If required columns are missing, predictions contain null values,
            or prediction values are not binary.

    Author:
        Jakub Vašák

    """
    missing_columns = [
        column for column in REQUIRED_PREDICTION_COLUMNS if column not in predictions
    ]
    if missing_columns:
        raise ValueError(
            f"Prediction CSV is missing required columns: {', '.join(missing_columns)}"
        )

    prediction_values = predictions[PREDICTION_COLUMN]
    if prediction_values.isna().any():
        raise ValueError(f"Column {PREDICTION_COLUMN!r} must not contain null values.")

    invalid_values = prediction_values.loc[~prediction_values.isin([0, 1, False, True])]
    if not invalid_values.empty:
        unique_values = sorted({str(value) for value in invalid_values.unique()})
        raise ValueError(
            f"Column {PREDICTION_COLUMN!r} must contain only binary values; "
            f"got invalid values: {', '.join(unique_values)}"
        )


def get_box_columns(predictions: pd.DataFrame) -> list[str]:
    """Return BOX columns in numeric order.

    Args:
        predictions: Predictions DataFrame with BOX<n> columns.

    Returns:
        List of BOX column names sorted by their numeric index.

    Raises:
        ValueError: If no BOX<n> column is found.

    Author:
        Jakub Vašák

    """
    box_columns = [
        column for column in predictions.columns if BOX_COLUMN_PATTERN.fullmatch(column)
    ]
    if not box_columns:
        raise ValueError("Prediction CSV must contain at least one BOX<n> column.")

    def box_column_number(column: str) -> int:
        match = BOX_COLUMN_PATTERN.fullmatch(column)
        if match is None:
            raise ValueError(f"Invalid BOX column name: {column}")
        return int(match.group("number"))

    return sorted(box_columns, key=box_column_number)


def collect_box_names(rows: pd.DataFrame, box_columns: list[str]) -> str:
    """Collect unique PNG names from BOX columns, preserving row and box order.

    Args:
        rows: Subset of the predictions DataFrame.
        box_columns: Ordered list of BOX column names.

    Returns:
        Semicolon-separated string of unique image filenames.

    Author:
        Jakub Vašák

    """
    box_names: list[str] = []
    seen_box_names: set[str] = set()

    for value in rows.loc[:, box_columns].to_numpy().reshape(-1).tolist():
        if pd.isna(value):
            continue

        box_name = str(value)
        if not box_name or box_name in seen_box_names:
            continue

        box_names.append(box_name)
        seen_box_names.add(box_name)

    return "; ".join(box_names)


def add_proxy_tolerance_columns(predictions: pd.DataFrame) -> pd.DataFrame:
    """Add directional proxy tolerance columns around the first positive row.

    Args:
        predictions: Full predictions DataFrame.

    Returns:
        Copy of ``predictions`` with four tolerance columns added.

    Author:
        Jakub Vašák

    """
    validate_prediction_schema(predictions)
    box_columns = get_box_columns(predictions)
    enriched = predictions.copy()
    enriched[PREDICTION_COLUMN] = enriched[PREDICTION_COLUMN].astype("uint8")
    for column in TOLERANCE_COLUMNS:
        enriched[column] = ""

    grouped_predictions = enriched.groupby(
        [TRAY_COLUMN, BOX_POSITION_COLUMN],
        sort=False,
    )
    for _group_key, group in grouped_predictions:
        group_indices = list(group.index)
        positive_positions = [
            position
            for position, prediction in enumerate(group[PREDICTION_COLUMN].tolist())
            if prediction == 1
        ]
        if not positive_positions:
            continue

        target_position = positive_positions[0]
        target_index = group_indices[target_position]
        for tolerance, plus_column, minus_column in (
            (1, PREDICTION_PLUS_ONE_COLUMN, PREDICTION_MINUS_ONE_COLUMN),
            (2, PREDICTION_PLUS_TWO_COLUMN, PREDICTION_MINUS_TWO_COLUMN),
        ):
            previous_indices = group_indices[
                max(target_position - tolerance, 0) : target_position
            ]
            next_indices = group_indices[
                target_position + 1 : min(
                    target_position + tolerance + 1, len(group_indices)
                )
            ]
            enriched.loc[target_index, minus_column] = collect_box_names(
                enriched.loc[previous_indices],
                box_columns,
            )
            enriched.loc[target_index, plus_column] = collect_box_names(
                enriched.loc[next_indices],
                box_columns,
            )

    return enriched


def _validate_tolerance_columns(predictions: pd.DataFrame) -> None:
    """Raise ValueError if any tolerance column is absent from the DataFrame."""
    missing_columns = [
        column for column in TOLERANCE_COLUMNS if column not in predictions
    ]
    if missing_columns:
        raise ValueError(
            "Prediction DataFrame is missing tolerance columns: "
            f"{', '.join(missing_columns)}"
        )


def build_first_germination_with_tolerance(
    predictions_with_tolerance: pd.DataFrame,
) -> pd.DataFrame:
    """Return first positive prediction per tray and box with tolerance columns.

    Args:
        predictions_with_tolerance: Predictions DataFrame with tolerance columns.

    Returns:
        Subset of ``predictions_with_tolerance`` containing the earliest positive
        prediction per (TRAY, BOX_POSITION) group, or an empty DataFrame if none.

    Author:
        Jakub Vašák

    """
    validate_prediction_schema(predictions_with_tolerance)
    _validate_tolerance_columns(predictions_with_tolerance)
    positives = predictions_with_tolerance.loc[
        predictions_with_tolerance[PREDICTION_COLUMN] == 1
    ]
    if positives.empty:
        return predictions_with_tolerance.iloc[0:0].copy()

    return (
        positives.groupby([TRAY_COLUMN, BOX_POSITION_COLUMN], as_index=False, sort=True)
        .first()
        .sort_values([TRAY_COLUMN, BOX_POSITION_COLUMN], kind="stable")
        .reset_index(drop=True)
    )


def load_prediction_csv(predictions_csv: Path) -> pd.DataFrame:
    """Load the existing full-prediction CSV.

    Args:
        predictions_csv: Path to the predictions CSV file.

    Returns:
        Loaded predictions DataFrame.

    Raises:
        FileNotFoundError: If the file does not exist.

    Author:
        Jakub Vašák

    """
    if not predictions_csv.is_file():
        raise FileNotFoundError(f"Prediction CSV not found: {predictions_csv}")
    return pd.read_csv(predictions_csv)


def save_postprocessed_prediction_outputs(
    predictions_with_tolerance: pd.DataFrame,
    first_germination_with_tolerance: pd.DataFrame,
    paths: PredictionPostprocessingPaths,
) -> PostprocessedPredictionOutputs:
    """Write post-processed prediction outputs as CSV and XLSX files.

    Args:
        predictions_with_tolerance: Post-processed predictions DataFrame.
        first_germination_with_tolerance: First-germination DataFrame with tolerance.
        paths: Configured post-processing paths.

    Returns:
        ``PostprocessedPredictionOutputs`` dataclass with paths to written files.

    Author:
        Jakub Vašák

    """
    paths.output_dir.mkdir(parents=True, exist_ok=True)
    outputs = postprocessed_prediction_output_paths(paths)
    predictions_with_tolerance.to_csv(outputs.predictions_csv, index=False)
    predictions_with_tolerance.to_excel(outputs.predictions_xlsx, index=False)
    first_germination_with_tolerance.to_csv(
        outputs.first_germination_csv,
        index=False,
    )
    first_germination_with_tolerance.to_excel(
        outputs.first_germination_xlsx,
        index=False,
    )
    return outputs


def run_prediction_postprocessing(
    paths: PredictionPostprocessingPaths,
) -> PostprocessedPredictionOutputs:
    """Run prediction post-processing and return output paths.

    Args:
        paths: Configured post-processing paths.

    Returns:
        ``PostprocessedPredictionOutputs`` dataclass with paths to all written files.

    Author:
        Jakub Vašák

    """
    predictions_csv = input_predictions_path(paths)
    logger.info("Loading emergence predictions from %s", predictions_csv)
    predictions = load_prediction_csv(predictions_csv)
    predictions_with_tolerance = add_proxy_tolerance_columns(predictions)
    first_germination_with_tolerance = build_first_germination_with_tolerance(
        predictions_with_tolerance
    )
    outputs = save_postprocessed_prediction_outputs(
        predictions_with_tolerance=predictions_with_tolerance,
        first_germination_with_tolerance=first_germination_with_tolerance,
        paths=paths,
    )
    logger.info("Post-processed predictions saved to %s", outputs.predictions_csv)
    logger.info(
        "Post-processed first-germination table saved to %s",
        outputs.first_germination_csv,
    )
    return outputs



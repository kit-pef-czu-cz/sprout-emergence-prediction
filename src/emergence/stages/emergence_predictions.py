"""Run temporal emergence predictions from the stage-3 NumPy datasets."""

from __future__ import annotations

import logging
from dataclasses import dataclass
from typing import TYPE_CHECKING, Any

import numpy as np
import pandas as pd
from tcn import TCN
from tensorflow import keras  # type: ignore

from emergence.path_config import (
    CONFIG_PATH,
    load_stage_config,
    resolve_config_path,
)
from emergence.path_config import (
    get_required_int as get_config_int,
)
from emergence.path_config import (
    get_required_string as get_config_string,
)
from emergence.path_config import (
    resolve_project_path as resolve_shared_project_path,
)

if TYPE_CHECKING:
    from pathlib import Path

logger = logging.getLogger(__name__)

DEFAULT_IMAGE_SIZE: int = 75
PREDICTION_THRESHOLD: float = 0.5


@dataclass(frozen=True)
class EmergencePredictionPaths:
    """Filesystem paths required by the emergence-prediction stage."""

    model_path: Path
    input_dir: Path
    output_dir: Path
    time_steps: int
    data_range: int


@dataclass(frozen=True)
class DatasetSpec:
    """Resolved dataset selection for stage-4 predictions."""

    image_size: int
    time_steps: int
    data_range: int
    data_path: Path
    image_names_path: Path

    @property
    def file_suffix(self) -> str:
        """Return the shared dataset suffix used in stage-3 filenames."""
        return f"{self.image_size}_{self.time_steps}_{self.data_range}"

    @property
    def output_suffix(self) -> str:
        """Return the notebook-compatible suffix used in output file names."""
        return f"{self.time_steps}-{self.data_range}"


@dataclass(frozen=True)
class PredictionOutputs:
    """Output files generated by the emergence-prediction stage."""

    predictions_csv: Path
    predictions_xlsx: Path
    first_germination_csv: Path
    first_germination_xlsx: Path


def get_required_string(config: dict[str, Any], key: str, config_path: Path) -> str:
    """Return a required non-empty string value from a TOML mapping."""
    return get_config_string(config, key, config_path)


def get_required_int(config: dict[str, Any], key: str, config_path: Path) -> int:
    """Return a required integer value from a TOML mapping."""
    return get_config_int(config, key, config_path)


def resolve_project_path(
    project_root: Path, relative_path: str, key: str, config_path: Path
) -> Path:
    """Resolve a relative config value against the configured project root."""
    return resolve_shared_project_path(project_root, relative_path, key, config_path)


def load_emergence_prediction_paths(
    config_path: Path = CONFIG_PATH,
) -> EmergencePredictionPaths:
    """Load stage-4 paths from the shared TOML configuration."""
    context, emergence_config = load_stage_config("emergence_predictions", config_path)

    return EmergencePredictionPaths(
        model_path=resolve_config_path(
            context,
            get_required_string(emergence_config, "model_path", config_path),
            "paths.emergence_predictions.model_path",
        ),
        input_dir=resolve_config_path(
            context,
            get_required_string(emergence_config, "input_dir", config_path),
            "paths.emergence_predictions.input_dir",
            include_project_name=True,
        ),
        output_dir=resolve_config_path(
            context,
            get_required_string(emergence_config, "output_dir", config_path),
            "paths.emergence_predictions.output_dir",
            include_project_name=True,
        ),
        time_steps=context.time_steps,
        data_range=context.data_range,
    )


def parse_dataset_file_name(file_path: Path) -> tuple[str, int, int, int] | None:
    """Parse one stage-3 NumPy dataset file name."""
    file_stem = file_path.stem
    for prefix in ("data", "image_names"):
        prefix_with_separator = f"{prefix}_"
        if not file_stem.startswith(prefix_with_separator):
            continue

        numeric_parts = file_stem.removeprefix(prefix_with_separator).split("_")
        if len(numeric_parts) != 3:
            return None

        image_size_text, time_steps_text, data_range_text = numeric_parts
        try:
            return (
                prefix,
                int(image_size_text),
                int(time_steps_text),
                int(data_range_text),
            )
        except ValueError:
            return None

    return None


def select_dataset_spec(
    input_dir: Path,
    *,
    time_steps: int,
    data_range: int,
    default_image_size: int = DEFAULT_IMAGE_SIZE,
) -> DatasetSpec:
    """Resolve the stage-3 dataset pair that matches the configured window."""
    data_candidates: dict[int, Path] = {}
    image_name_candidates: dict[int, Path] = {}

    for file_path in sorted(input_dir.glob("*.npy")):
        parsed = parse_dataset_file_name(file_path)
        if parsed is None:
            continue

        prefix, image_size, file_time_steps, file_data_range = parsed
        if file_time_steps != time_steps or file_data_range != data_range:
            continue

        if prefix == "data":
            data_candidates[image_size] = file_path
        else:
            image_name_candidates[image_size] = file_path

    matching_image_sizes = sorted(set(data_candidates) & set(image_name_candidates))
    if not matching_image_sizes:
        expected_suffix = f"*_{time_steps}_{data_range}.npy"
        raise FileNotFoundError(
            "Could not find matching stage-3 dataset files in "
            f"{input_dir} for model window {time_steps}-{data_range}. "
            f"Expected files like data_{expected_suffix} and "
            f"image_names_{expected_suffix}."
        )

    if len(matching_image_sizes) == 1:
        selected_image_size = matching_image_sizes[0]
    elif default_image_size in matching_image_sizes:
        selected_image_size = default_image_size
        logger.info(
            "Multiple dataset image sizes found for model window %s-%s; using %s px",
            time_steps,
            data_range,
            selected_image_size,
        )
    else:
        raise ValueError(
            "Found multiple matching dataset image sizes and no default match: "
            f"{matching_image_sizes}"
        )

    return DatasetSpec(
        image_size=selected_image_size,
        time_steps=time_steps,
        data_range=data_range,
        data_path=data_candidates[selected_image_size],
        image_names_path=image_name_candidates[selected_image_size],
    )


def load_numpy_inputs(dataset_spec: DatasetSpec) -> tuple[np.ndarray, np.ndarray]:
    """Load the stage-3 NumPy data array and aligned image-name windows."""
    samples = np.load(dataset_spec.data_path)
    image_name_windows = np.load(dataset_spec.image_names_path)
    logger.info("Loaded samples with shape %s", samples.shape)
    logger.info("Loaded image-name windows with shape %s", image_name_windows.shape)
    return samples, image_name_windows


def validate_model_name_window(
    model_path: Path, *, time_steps: int, data_range: int
) -> None:
    """Ensure the configured model name contains the active window values."""
    model_name = model_path.name
    expected_tokens = (f"{time_steps}-{data_range}", f"{time_steps}_{data_range}")
    if not any(token in model_name for token in expected_tokens):
        raise ValueError(
            "Configured temporal model name must contain the configured "
            f"time_steps/data_range token ({time_steps}-{data_range} or "
            f"{time_steps}_{data_range}); got {model_name!r}."
        )


def load_temporal_model(model_path: Path, *, time_steps: int, data_range: int) -> Any:  # noqa: ANN401
    """Load the saved temporal model used for emergence prediction."""
    if not model_path.exists():
        raise FileNotFoundError(f"Temporal model not found: {model_path}")

    validate_model_name_window(
        model_path,
        time_steps=time_steps,
        data_range=data_range,
    )
    return keras.models.load_model(
        str(model_path),
        custom_objects={"TCN": TCN},
        compile=False,
    )


def predict_labels(
    model: Any,  # noqa: ANN401
    samples: np.ndarray,
    *,
    threshold: float = PREDICTION_THRESHOLD,
) -> list[int]:
    """Predict emergence labels and threshold them into binary outputs."""
    probabilities = model.predict(samples, verbose=0)
    prediction_labels = (probabilities >= threshold).astype(np.uint8)
    return prediction_labels.reshape(-1).tolist()


def extract_tray_and_box(box_name: str) -> tuple[int, str]:
    """Extract tray id and box position from a stage-3 image filename."""
    shards = box_name.split("_")
    if len(shards) < 2:
        raise ValueError(
            f"Expected an underscore-separated image file name, got: {box_name}"
        )
    tray_id = int(shards[0])
    box_position = shards[-1].replace(".png", "")
    return tray_id, box_position


def build_prediction_dataframe(
    class_labels: list[int], image_name_windows: np.ndarray, dataset_spec: DatasetSpec
) -> pd.DataFrame:
    """Build the full predictions table matching notebook-era outputs."""
    box_windows = image_name_windows.tolist()
    if len(class_labels) != len(box_windows):
        raise ValueError(
            "Predictions and image-name windows must have equal lengths, got "
            f"{len(class_labels)} predictions and {len(box_windows)} windows."
        )

    box_columns = [f"BOX{index}" for index in range(1, dataset_spec.time_steps + 1)]
    predictions_df = pd.DataFrame(
        {"PREDICTIONS": pd.Series(class_labels, dtype="uint8")}
    )
    boxes_df = pd.DataFrame(box_windows, columns=box_columns, dtype="string")
    result = pd.concat([predictions_df, boxes_df], axis=1)

    tray_box_pairs = result["BOX1"].map(extract_tray_and_box)
    result["TRAY"] = [tray_id for tray_id, _ in tray_box_pairs]
    result["BOX_POSITION"] = [box_position for _, box_position in tray_box_pairs]

    return result


def build_first_germination_dataframe(predictions: pd.DataFrame) -> pd.DataFrame:
    """Return the first positive prediction per tray and box position."""
    positives = predictions.loc[predictions["PREDICTIONS"] == 1]
    if positives.empty:
        return predictions.iloc[0:0].copy()

    return (
        positives.groupby(["TRAY", "BOX_POSITION"], as_index=False, sort=True)
        .first()
        .sort_values(["TRAY", "BOX_POSITION"], kind="stable")
        .reset_index(drop=True)
    )


def save_prediction_outputs(
    predictions: pd.DataFrame,
    first_germination: pd.DataFrame,
    output_dir: Path,
    dataset_spec: DatasetSpec,
) -> PredictionOutputs:
    """Write full and first-germination outputs as CSV and XLSX files."""
    output_dir.mkdir(parents=True, exist_ok=True)
    suffix = dataset_spec.output_suffix

    outputs = PredictionOutputs(
        predictions_csv=output_dir / f"predictions_model_{suffix}.csv",
        predictions_xlsx=output_dir / f"predictions_model_{suffix}.xlsx",
        first_germination_csv=output_dir / f"first_germination_model_{suffix}.csv",
        first_germination_xlsx=output_dir / f"first_germination_model_{suffix}.xlsx",
    )

    predictions.to_csv(outputs.predictions_csv, index=False)
    predictions.to_excel(outputs.predictions_xlsx, index=False)
    first_germination.to_csv(outputs.first_germination_csv, index=False)
    first_germination.to_excel(outputs.first_germination_xlsx, index=False)
    return outputs


def run_emergence_predictions(
    paths: EmergencePredictionPaths,
) -> tuple[DatasetSpec, PredictionOutputs]:
    """Run the stage-4 prediction pipeline and return output paths."""
    dataset_spec = select_dataset_spec(
        paths.input_dir,
        time_steps=paths.time_steps,
        data_range=paths.data_range,
    )
    samples, image_name_windows = load_numpy_inputs(dataset_spec)
    model = load_temporal_model(
        paths.model_path,
        time_steps=paths.time_steps,
        data_range=paths.data_range,
    )
    class_labels = predict_labels(model, samples)
    predictions = build_prediction_dataframe(
        class_labels, image_name_windows, dataset_spec
    )
    first_germination = build_first_germination_dataframe(predictions)
    outputs = save_prediction_outputs(
        predictions=predictions,
        first_germination=first_germination,
        output_dir=paths.output_dir,
        dataset_spec=dataset_spec,
    )
    return dataset_spec, outputs

